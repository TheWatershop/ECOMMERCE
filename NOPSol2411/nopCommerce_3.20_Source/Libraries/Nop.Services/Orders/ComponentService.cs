        /// <summary>
        /// Get orders by identifiers
        /// </summary>
        /// <param name="orderIds">Order identifiers</param>
        /// <returns>Order</returns>
        public virtual IList<ComponentItem> GetComponentsByIds(int[] orderIds)
        {
            if (orderIds == null || orderIds.Length == 0)
                return new List<ComponentItem>();

            var query = from o in _ComponentItemRepository.Table
                        where orderIds.Contains(o.Id)
                        select o;
            var Components = query.ToList();
            //sort by passed identifiers
            var sortedComponents = new List<ComponentItem>();
            foreach (int id in orderIds)
            {
                var Component = Components.Find(x => x.Id == id);
                if (Component != null)
                    sortedComponents.Add(Component);
            }
            return sortedComponents;
        }

